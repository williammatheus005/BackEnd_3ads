function divisao(n1, n2) {
    if(n2 != 0) {
        console.log("a divisão de " + n1 + " por " + n2 + " é igual a " +  n1 / n2)
    }
    else {
        console.error("impossível dividir.") 
    }
}
divisao(12,2)
divisao(12,0)

